base = float(input("Ingrese la medida de la base de la pirámide: ")) #se difine variable tipo decimal con entrada de dat
altura = float(input("Ingrese la medida de la altura de la pirámide: ")) #se difine variable tipo decimal con entrada de dat

volumen = (1/3) * base**2 * altura #operacion para calcular el volumen de una piramide que sera guardado en volumen

print("El volumen de la pirámide es:", volumen) #se imprime resultado